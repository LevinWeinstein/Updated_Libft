#include "libft.h"

void	ft_putnbr(int n)
{
	int		negative;

	if ((negative = (n < 0 ? -1 : 1)) == -1)
		ft_putchar('-');
	if (n >= 10 || n <= -10)
		ft_putnbr((n / 10) * negative);
	ft_putchar('0' + ((n % 10) * negative));
}
